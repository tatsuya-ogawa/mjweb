import { execFile } from "node:child_process";
import { mkdir, readFile, readdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { promisify } from "node:util";

const rootDir = path.resolve(fileURLToPath(new URL("..", import.meta.url)));
const publicDir = path.join(rootDir, "public");
const publicEnvDir = path.join(publicDir, "envs");
const outputPath = path.join(rootDir, "src", "generated", "gaussianSources.ts");
const supportedExtensions = new Set([".sog", ".spz", ".splat", ".ply"]);
const execFileAsync = promisify(execFile);
const includeLocalGaussian = process.env.MJWEB_INCLUDE_LOCAL_GAUSSIAN === "1";

const trackedSources = includeLocalGaussian ? null : await collectTrackedSources();
const sourceUrls = await collectGaussianSources(publicEnvDir, trackedSources);
sourceUrls.sort((a, b) => a.localeCompare(b));
const externalSourceConfigs = await collectExternalGaussianSourceConfigs(publicEnvDir);
externalSourceConfigs.sort((a, b) =>
  a.envId.localeCompare(b.envId) || a.bundleId.localeCompare(b.bundleId)
);

const contents = `// Generated by scripts/generate-gaussian-source-manifest.mjs. Do not edit by hand.
export const bundledGaussianSourceUrls = ${JSON.stringify(sourceUrls, null, 2)} as const;

export const externalGaussianSourceConfigs = ${JSON.stringify(externalSourceConfigs, null, 2)} as const;
`;

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, contents);

async function collectTrackedSources() {
  try {
    const { stdout } = await execFileAsync("git", ["ls-files", "-z", "--", "public/envs"], {
      cwd: rootDir,
    });
    return new Set(stdout.split("\0").filter(Boolean));
  } catch {
    return null;
  }
}

async function collectGaussianSources(dir, trackedSources) {
  let entries = [];
  try {
    entries = await readdir(dir, { withFileTypes: true });
  } catch (error) {
    if (error?.code === "ENOENT") {
      return [];
    }
    throw error;
  }

  const urls = [];
  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      urls.push(...await collectGaussianSources(fullPath, trackedSources));
      continue;
    }
    if (!entry.isFile() || !supportedExtensions.has(path.extname(entry.name).toLowerCase())) {
      continue;
    }
    const relativePublicPath = path.relative(publicDir, fullPath).split(path.sep).join("/");
    if (!relativePublicPath.split("/").includes("splats")) {
      continue;
    }
    if (trackedSources && !trackedSources.has(`public/${relativePublicPath}`)) {
      continue;
    }
    urls.push(`/${relativePublicPath}`);
  }
  return urls;
}

async function collectExternalGaussianSourceConfigs(dir) {
  let entries = [];
  try {
    entries = await readdir(dir, { withFileTypes: true });
  } catch (error) {
    if (error?.code === "ENOENT") {
      return [];
    }
    throw error;
  }

  const configs = [];
  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      configs.push(...await collectExternalGaussianSourceConfigs(fullPath));
      continue;
    }
    if (!entry.isFile() || entry.name !== "transform.json") {
      continue;
    }
    const relativePublicPath = path.relative(publicDir, fullPath).split(path.sep).join("/");
    const parsed = parseGaussianTransformPath(relativePublicPath);
    if (!parsed) {
      continue;
    }
    const transformConfig = await readTransformConfig(fullPath);
    if (!transformConfig) {
      continue;
    }
    const sourceUrls = sourceUrlsFromTransformConfig(transformConfig);
    if (sourceUrls.length === 0) {
      continue;
    }
    configs.push({
      envId: parsed.envId,
      bundleId: parsed.bundleId,
      sourceUrls,
      transformConfig,
    });
  }
  return configs;
}

async function readTransformConfig(filePath) {
  try {
    return JSON.parse(await readFile(filePath, "utf8"));
  } catch (error) {
    throw new Error(`Failed to parse ${filePath}: ${error instanceof Error ? error.message : String(error)}`);
  }
}

function parseGaussianTransformPath(relativePublicPath) {
  const parts = relativePublicPath.split("/").filter(Boolean);
  const envIndex = parts.indexOf("envs");
  const splatsIndex = parts.indexOf("splats");
  const afterSplats = splatsIndex >= 0 ? parts.slice(splatsIndex + 1) : [];
  if (
    envIndex < 0 ||
    splatsIndex < 0 ||
    afterSplats.length !== 2 ||
    afterSplats[1] !== "transform.json"
  ) {
    return null;
  }
  return {
    envId: parts[envIndex + 1],
    bundleId: afterSplats[0],
  };
}

function sourceUrlsFromTransformConfig(transformConfig) {
  const urls = [];
  if (typeof transformConfig.sourceUrl === "string") {
    urls.push(transformConfig.sourceUrl);
  }
  if (Array.isArray(transformConfig.sourceUrls)) {
    urls.push(...transformConfig.sourceUrls.filter((url) => typeof url === "string"));
  }
  return [...new Set(urls)].filter(isSupportedGaussianSourceUrl);
}

function isSupportedGaussianSourceUrl(url) {
  const fileName = sourceFileName(url).toLowerCase();
  return supportedExtensions.has(path.extname(fileName));
}

function sourceFileName(url) {
  try {
    return path.posix.basename(new URL(url).pathname);
  } catch {
    return url.split(/[?#]/, 1)[0].split("/").pop() ?? url;
  }
}
